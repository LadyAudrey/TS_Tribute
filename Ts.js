// when an album img is clicked, an h3 and p element are created and populated with strings
const buttonClicked = (album, OGvsTV, info) => {
  albumInfo.replaceChildren();
  const albumInfoH3 = albumInfo.appendChild(document.createElement("h3"));
  const releasedInfo = albumInfo.appendChild(document.createElement("h4"));
  const albumInfoP = albumInfo.appendChild(document.createElement("p"));
  const quotes = albumInfo.appendChild(document.createElement("h4"));
  albumInfoH3.textContent = album;
  quotes.textContent = info.quotes;
  albumInfoP.textContent = info.trackList;
  if (OGvsTV === "OG") {
    releasedInfo.textContent = info.released[0];
  } else if (OGvsTV === "TV") {
    releasedInfo.textContent = info.released[1];
  } else if (OGvsTV === "TayOwned") {
    releasedInfo.textContent = "Released on" + info.released;
  } else {
    console.log("OGvsTV tag is not working right");
  }
};

